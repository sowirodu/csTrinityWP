function sortTable(columnNumber) {
    var switching, cleanRows, i, x, y, shouldSwitch, dir, switchcount = 0;
    switching = true;
    dir = "desc"

    while(switching){
        switching = false
        cleanRows = table.rows
        for(i = 1; i < cleanRows.length -1; i++) {
            shouldSwitch = false

            x = cleanRows[i].getElementsByTagName("td")[columnNumber]
            y = cleanRows[i + 1].getElementsByTagName("td")[columnNumber] 
            xVal = isNaN(Number(x.innerHTML)) ? x.innerHTML.toLowerCase() : Number(x.innerHTML);
            yVal = isNaN(Number(y.innerHTML)) ? y.innerHTML.toLowerCase() : Number(y.innerHTML);

            if (dir == "asc") {
                if (xVal > yVal) {
                    shouldSwitch = true;
                    break;
                }
            } else if (dir == "desc") {
                if (xVal < yVal) {
                    shouldSwitch = true;
                    break;
                }
            }
        }
        if (shouldSwitch) {
            cleanRows[i].parentNode.insertBefore(cleanRows[i+1], cleanRows[i])
            switching = true

            switchcount++
        } else {
            if (switchcount == 0 && dir == "asc"){
                dir = "desc"
                switching = true
            }

            switchcount = 0
        }
    }


}

